// Note: this is used by cameras-media-server. 
// The ffmpeg process publishes the videos to this server.

const NodeMediaServer = require('node-media-server');
 
const config = {
  logType: 3,
  rtmp: {
    port: 1935,
    chunk_size: 60000,
    gop_cache: true,
    ping: 30,
    ping_timeout: 60
  },
  http: {
    port: 8000,
    allow_origin: '*'
  }
};
 
var nms = new NodeMediaServer(config)
nms.run();
